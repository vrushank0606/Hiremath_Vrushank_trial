    private void txtOwnerTelephoneNumberKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtOwnerTelephoneNumberKeyPressed
        // TODO add your handling code here:
        String Telephonenumber = txtOwnerTelephoneNumber.getText();
        //get length of a string
        int length =Telephonenumber.length();
        
        char c = evt.getKeyChar();
        
        //Check for number 0-9
        
        if(evt.getKeyChar()>='0' && evt.getKeyChar()<='9'){
            if (length<10){
                //editable true
                txtOwnerTelephoneNumber.setEditable(true);
                lblnumerr.setText("Please enter Phone No only!");
            }else{
                //not editable for length more than 10 digits 
                txtOwnerTelephoneNumber.setEditable(false);
                lblnumerr.setText(" ");
            }
        }else{
            //allow  keys 'back space' and 'delete'
            if(evt.getExtendedKeyCode() == KeyEvent.VK_BACK_SPACE || evt.getExtendedKeyCode() == KeyEvent.VK_DELETE){
                //allow editable
                txtOwnerTelephoneNumber.setEditable(true);
                
            }else{
                txtOwnerTelephoneNumber.setEditable(false);
            }
                
            
            
        }
           
    }//GEN-LAST:event_txtOwnerTelephoneNumberKeyPressed

