   private void Seat_Number_Validation(){
       try 
        {
            Integer.parseInt(txtSeatNumber.getText());
            error = "";
        } 
        catch (NumberFormatException e) 
        {
            error = "Enter Number Only";
        }
          
   }
   
   private void Telephone_Number_Validation(){
       try 
        {
            Integer.parseInt(txtOwnerTelephoneNumber.getText());
            if (txtOwnerTelephoneNumber.getText().length()<10 || txtOwnerTelephoneNumber.getText().length()<10){
                error = "Enter Correct Number";
                
            }
            else{
                error = "";
            }
            error = "";
        } 
        catch (NumberFormatException e) 
        {
            error = "Enter Number Only";
        }
       
   }
   
